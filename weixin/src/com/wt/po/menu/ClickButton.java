package com.wt.po.menu;
/**
 * @Description:click类型的按钮
 * @author wt
 * @date 2018年5月31日
 */
public class ClickButton extends Button {
	private String type;
	private String key;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	
}
